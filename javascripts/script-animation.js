const debounce = function(func, wait, immediate){
  let timeout;
  return function(...args) {
    const context = this;
    const later = function () {
      timeout = null;
      if(!immediate) func.apply(context, args);
    };
    const callNow = immediate && !timeout;
    clearTimeout(timeout);
    timeout = setTimeout(later, wait);
    if(callNow) func.apply(context, args);
  };
};

const animTop = document.querySelectorAll('.balloon');
const animMiddle = document.querySelectorAll('.price-box');
const animBottom = document.querySelectorAll('.contact-a');

function animationScroll(){
  const windowTop = window.pageYOffset +((window.innerHeight *3)/4) ;
  animTop.forEach(element => {
    if(windowTop > element.offsetTop){
      lottie.play(`${element.children[0].id}`);
    }else{
      lottie.goToAndStop(1, false, `${element.children[0].id}`);
    }
  })
  animMiddle.forEach(ele => {
    if(windowTop > ele.offsetTop){
      lottie.play(`${ele.children[0].children[0].id}`);
    }else{
      lottie.goToAndStop(1, false, `${ele.children[0].children[0].children[0].id}`);
    }
  })
  animBottom.forEach(e => {
    if(windowTop > e.offsetTop){
      lottie.play(`${e.children[0].children[0].id}`);
    }else{
      lottie.goToAndStop(1, false, `${e.children[0].children[0].id}`);
    }
  })
}

animationScroll();

if(animTop.length || animMiddle.length || animBottom.length) {
  window.addEventListener('scroll', debounce(function() {
    animationScroll();
  }, 200));
}
